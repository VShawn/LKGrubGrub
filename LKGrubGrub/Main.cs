“会在１００年后”<br />
“那样的话，就一下子而已。吾会乖乖地等着”<br />
<br />
巴蒂加迪这么说完，又躺了下来。<br />
奥尔斯蒂德点完头，也转过去身。<br />
这样对话就结束了吗。<br />
真干脆呢。<br />
<br />
“陛下……虽然变成这种情况了才说这些，不过在魔法大学时，许多事都非常的感谢您”<br />
“嗯。卢迪乌斯啊，虽然不知道是不是最后了，不过先跟你说声恭喜”<br />
“恭喜？”<br />
“因为你赢了，所以恭喜”<br />
“真的，算是赢了吗……”<br />
<br />
我烦恼的地方在这里。<br />
结果，奥尔斯蒂德用到了魔力。<br />
在最后的最后，搞砸了。<br />
但是，巴蒂加迪并没有提到那部份。<br />
<br />
“嗯。你给了人神败北感”<br />
“败北感？”<br />
“嗯。你让人神觉得“不管做什么都杀不了这家伙”了。人神已经完全失去干劲了。吾最后看到的人神的样貌虽然难以用口头说明，但那几乎就是输家的样子。那么，他的对手要是不算赢的话，又算什么呢”<br />
“……那些，是真的吗？”<br />
“不信的话，把那护手脱掉，去见一次就知道了”<br />
<br />
被他一指，我下意识地用手遮住了护手。<br />
<br />
“这……还是先算了吧”<br />
“是吗，这样也好”<br />
<br />
这方法还是算了。<br />
我已经完全不想再见到人神了。<br />
不过。<br />
在谷底见到时，的确是已经山穷水尽了的感觉。<br />
这次的胜利，让人神产生了巨大的败北感的这件事，或许是真的。<br />
还是，失去干劲了什么也不会做了的这部份，不能相信就是了。<br />
<br />
“话讲完了吗？”<br />
“我这边已经说完了”<br />
“是吗。那，保重啊”<br />
<br />
跟着奥尔斯蒂德的脚步，我也转身。<br />
那时，一脸如坐针毡的表情的亚历跳了过来。<br />
<br />
“舅公大人……我──”<br />
“亚历山大啊。要是你想成为英雄的话，就去寻找自己真正的敌人吧。因为你的父亲，一直以来都没能找到。当打倒那个敌人的时候，你就能成为超越父亲的英雄了吧”<br />
“……我明白了”<br />
<br />
亚历也一样转过身。<br />
<br />
我大概在此就和巴蒂加迪是一生之别了。<br />
虽然也可以每隔几年就来露个面，但聊着聊着，说不定我就把封印给解除了。<br />
那么，还是不要来比较好。<br />
也没有告诉魔法大学出身的其他人，巴蒂加迪被封印在这里。<br />
知道这个地方的人，只有我和奥尔斯蒂德、瑞杰尔德、亚历、佩尔基乌斯五个人而已。<br />
<br />
而且瑞杰尔德已经在村子的入口，为了避免有人来这座峡谷而替我监视了。<br />
再加上，能够下到这地龙谷底的人，和能爬上去的人都不多。<br />
区区１００年的话，偶然地被解开封印的事，不会发生吧。<br />
而且，更何况──。<br />
<br />
“卢迪乌斯，入口”<br />
“是”<br />
<br />
小小地挖出的入口被埋了起来。<br />
只要没有特地到处开挖，就不会被发现。<br />
再见了。<br />
<br />
“年轻的龙神啊。但愿你的诅咒能够化解”<br />
<br />
最后感觉好像隐约的听到了巴蒂加迪的声音。<br />
<br />
---<br />
<br />
隔天。<br />
一大清早，在太阳东升之前我回到了夏利亚。<br />
<br />
建筑当中的事务所。<br />
只剩瓦砾的前事务所。<br />
在这里，好像负责了建筑指挥的扎诺巴他们正睡成一排。<br />
这次也受扎诺巴照顾了。<br />
希望跟他以后也能继续保持这互相支持的关系。<br />
<br />
“那，卢迪乌斯。往后也麻烦你了”<br />
<br />
还有，和奥尔斯蒂德也是呢。<br />
<br />
“是”<br />
<br />
我在城外和奥尔斯蒂德分别，在晨雾中的城里走着。<br />
手上是毕坚利鲁王国的土产。<br />
特别是，酱油的存在无法忽视。<br />
在往后的日子里，只要有这瓶酱油在，我就能活上一辈子吧。<br />
<br />
和过去没有改变的，夏利亚的市区。<br />
人的样貌也没有改变。<br />
现在正前往农耕的人，在旅馆的广场辛勤锻炼的冒险者。<br />
大概是大学教师吧，还能看到穿着长袍的男性。<br />
我一边和他们擦身而过，一边踏上积雪的回家之路。<br />
穿过中央广场，往居住区。<br />
那个景象不知为何的让人感到怀念。<br />
虽然是几乎每天都在走的道路，但为什么呢，让人涌现出总算回来了的心情。<br />
<br />
我从马路走进小巷。<br />
那是马车也无法通过的窄道。<br />
只能稍微抄上一点近路的这条路，是我常常走的路。<br />
一走出小巷，便看见了我的家。<br />
<br />
缠绕在门柱上的彼多，当我一接近，便替我打开了门。<br />
庭园里，有着稍微显得有些缺乏修整的家庭菜园。<br />
犰狳的吉罗看到了我，便走到我身旁磨蹭。<br />
我蹲下来摸摸它的头后，它便翻过来露出的肚子。真是可爱的小东西。<br />
<br />
那时，玄关的门悄悄地打开了。<br />
走出来的是圣兽雷欧，还有艾丽丝。<br />
<br />
“啊，卢迪乌斯。欢迎回来”<br />
“我回来了。大家呢？”<br />
“平安无事唷”<br />
“我不是问这个，在做什么？”&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 　 妈<br />
“……莉莉娅和希露菲在准备早餐，洛克希和孩子们还有婆婆还在睡觉。而我现在，正准备要稍微跑一下喔”<br />
“是吗”<br />
<br />
我嘟嚷一声后，握起了艾丽丝的手。<br />
艾丽丝也像是回应我一样，紧紧地回握我的手。<br />
是刚空挥完吗，有点温暖。<br />
仔细一看，艾丽丝的脸也稍微有点红润。<br />
<br />
“干、干嘛？”<br />
“艾丽丝。今天就当休息日吧”<br />
“知、知道了啦”<br />
<br />
就像是在说已经看穿我想要做什么一样的“知道了啦”。<br />
完全正确。<br />
<br />
“雷欧，抱歉啊，散步取消了”<br />
“……汪呜”<br />
<br />
雷欧虽然露出有点遗憾的表情，但舔了舔我另一只手后，便回到了房子里。<br />
我和艾丽丝手牵着手，走进了家里。<br />
然后就这样走向厨房。<br />
在厨房里，莉莉娅和希露菲正一左一右的料理当中。<br />
<br />
“我回来了”<br />
“啊，欢迎回来卢迪”<br />
“欢迎您的归来，少爷”<br />
<br />
露出了一如以往的笑容给我看的希露菲，还有露出放心的表情的莉莉娅。<br />
<br />
“莉莉娅小姐，负责看家真是辛苦你了”<br />
“不会。少爷能够平安回来才是最重要的”<br />
“诺伦和爱莎好像还会在那边待一阵子”<br />
“我知道了”<br />
<br />
莉莉娅低头行礼后，我转头和希露菲面对面。<br />
<br />
“希露菲，今天就当休息日吧”<br />
“欸？　可以是可以不过休息日是……”<br />
<br />
歪头苦思的希露菲。<br />
不过，莉莉娅好像马上就想到了。<br />
<br />
“明白了。夫人，料理就由我来负责”<br />
“啊……是这么一回事啊”<br />
<br />
希露菲腼腆地笑着，握起了和艾丽丝反方向的手。<br />
大概是因为正在料理，有沾到水的关系，有一点冰冷。<br />
<br />
“莉莉娅小姐。中午大家到外面吃吧”<br />
“谨遵您吩咐”<br />
<br />
就像是在说已经看穿了一切的笑容。<br />
果然还是有一点害羞。<br />
不过，不算什么，都事到如今了。<br />
<br />
希露菲和艾丽丝，我一边握着二人的手，一边走向儿童房。<br />
轻声打开房门往里面一看，四个小孩正安安稳稳地睡着。<br />
露西、菈菈、亚尔斯、齐格。<br />
像是在保护他们一般，雷欧正在房间的一角卷成一圈。<br />
在这次战斗当中，无数次地担心着家里的事情。<br />
但，我的担心是多余的，孩子们非常地平稳。或许在我不知道的地方，发生过战斗，而雷欧替我保护了他们也说不定就是了……。<br />
总而言之，我确认完孩子们的平安健康后，又静静地关上房门。<br />
<br />
接着，爬上楼梯，走到洛克希的房间。<br />
基于礼貌，敲了敲门。<br />
<br />
“……来了”<br />
<br />
数秒之后，传来了回应。<br />
门一打开，睡眼惺忪的洛克希便映入眼廉。<br />
头发乱翘，嘴角还留有口水的痕迹。<br />
<br />
“啊啊……卢迪。欢迎回来”<br />
“我回来了。洛克希。其实今天我想当做休息日，不知道行不行呢”<br />
<br />
洛克希发愣了几秒后，理解了休息日的意思。<br />
急忙用手指梳平着翘起的前发，脸颊也变得有些泛红。<br />
<br />
“我是无所谓啦……”<br />
<br />
我看向我的左手和右手，在那彼端的二名女性之中的，其中一边。<br />
<br />
“艾丽丝，你清楚了吗？”<br />
<br />
看向艾丽丝。<br />
她正用有些摸不着头绪的脸，而脸上正发着红。<br />
<br />
“我正想问”<br />
<br />
我重新转向艾丽丝。<br />
<br />
“艾丽丝，接下来我想四个人一起去寝室，可以吗？”<br />
<br />
一问，艾丽丝好像就明白意思了。<br />
脸颊又变得更通红，嘴巴也嘟了起来。<br />
要是双手空着的话，又会摆出招牌动作了吧。<br />
<br />
“如果卢迪乌斯，无论如何也要的话…………”<br />
<br />
抱歉啊艾丽丝。<br />
今天稍微想要给自己一点奖励。<br />
想要跟禁欲的卢迪乌斯说拜拜了。<br />
<br />
“谢谢”<br />
<br />
我道了谢。<br />
不光是艾丽丝答应我的这件事。<br />
也是对着至今一直支持着我的三个人，说的。<br />
更是对没有失去任何就结束的战斗，表示感谢。<br />
<br />
基司和巴蒂加迪已经说了。<br />
说这样就结束了。<br />
说人神已经不会对我出手了。<br />
当然，那种话可不能相信。<br />
只要人神还活着，就要面对我这个敌人。<br />
<br />
但是，今天一天就休息吧。<br />
为了取得明天的活力，为了再次度过平静的一天。<br />
为了让笑容更加的有实感──。<br />
<br />
才─怪。<br />
单纯只是我想Ｈ而已。<br />
来吧，今天开始我就是解禁的卢迪乌斯啦。<br />
<br />
我一边这么想着，一边四个人一起走向寝室。<br />
<br />
<br />
