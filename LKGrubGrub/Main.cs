第二百五十九话“战斗的终结”&nbsp; &nbsp; &nbsp; &nbsp; translated by Wix Litariz<br />
<br />
从那之后经过了一个月。<br />
<br />
我现在，正在地龙谷之森的出口附近。<br />
<br />
四周围，排着一栋栋简易地盖起的木造住家，<br />
在砍去树木而辟开的广场中，各式各样的人们正忙碌地走来走去。<br />
斯佩尔多族及从毕坚利鲁王国雇来的人族的木工和壮丁、樵夫还有……卢多佣兵团。<br />
<br />
“哥哥，东边的森林能够帮忙再多开拓一点吗？”<br />
<br />
爱莎也在。<br />
她正在村子里一面大步地走着，一面朝各个地方发出指示。<br />
莉妮亚和普尔赛娜，则按照指示指挥着团员，这种感觉。<br />
这样一来，都搞不清楚谁是团长了呢。<br />
<br />
“嗯，知道了”<br />
<br />
我也混在他们里面，正在复兴斯佩尔多族之村。<br />
时而用魔法开拓森林，时而用土魔法造出房子的地基，时而铺出往地龙谷之村的道路。<br />
要做的事情成山成海。<br />
<br />
好了。<br />
<br />
为什么爱莎与卢多佣兵团会在这里呢。<br />
为什么亚历来的时候，除了奥尔斯蒂德之外谁都不在呢。<br />
这些事不说明一下可不行。<br />
<br />
只不过，要说明的话一句话就够了。<br />
那是爱莎的杰作。<br />
说是杰作好像干出了什么坏事一样，所以换成工作吧。<br />
那是爱莎的工作。<br />
<br />
转移魔法阵与通信石版停止时。<br />
爱莎与佣兵团也陷入了极端的混乱。<br />
在遥远的异国土地被封印了联络与通行手段，涌现出的不安与焦虑。<br />
在那时候，爱莎依然冷静。<br />
冷静地判断状况，开始思考。<br />
要是现场开始战斗的话，在国境附近的自己一行人就算朝现场去，不只太慢了，能做的事也不多，她这么想。<br />
<br />
爱莎所得出的结论，是一边考虑着基司逃脱的可能性，一边尽力去复原转移魔法阵。<br />
简单来说，就是修复设施。<br />
<br />
虽说如此，不光是转移魔法阵，连爱莎所带着的备用的魔法阵所对应的事务所的魔法阵也全部都被破坏了。<br />
无计可施。<br />
我的话到这里就会放弃了吧。<br />
实际上，也是放弃了。<br />
<br />
然而爱莎想到了。<br />
她那天才般的头脑想起来了。某个人的秘术。<br />
那个秘术，即是掌握住其中一边失效的转移魔法阵，然后画出对应的魔法阵，来移动到想去的地方的技术。<br />
<br />
某个人是指谁呢。<br />
没错，那便是‘甲龙王’佩尔基乌斯．朵拉。<br />
<br />
她为了前去拜托佩尔基乌斯，而在国境附近寻找七大列强的石碑。<br />
发现之后，利用佩尔基乌斯之笛前往空中城塞。<br />
<br />
知道了我们打算帮助魔族而面露难色的佩尔基乌斯。<br />
他对着爱莎说“吾只帮你接起一组”，而爱莎选择了接起国境附近的魔法阵与通往斯佩尔多族之村的转移魔法阵。<br />
就是这么回事。<br />
<br />
“真亏你能让佩尔基乌斯大人同意呢”<br />
“他那时候脸色超级难看的就是了呢，不过告诉他在这场战斗中能让奥尔斯蒂德欠下人情之后，就答应了唷”<br />
<br />
之后，在我打得正激烈时前往斯佩尔多族之村。<br />
听完情况后马上利用转移魔法阵，让居民及其他人到国境附近的城镇避难……这样。<br />
<br />
不过要是回到夏利亚的洛克希不是先召唤魔导铠零式，而是以普通的转移魔法阵优先的话，就差一点要做白工了……。<br />
巧合地，感觉好像洛克希的失误由爱莎弥补了一样。<br />
虽然是莫可奈何的事，不过这方面洛克希也感到相当惭愧，沮丧了一段时间。<br />
<br />
“这一带？”<br />
“嗯，一口气铲平吧。尽量弄得开阔一点还是比较好对吧？”<br />
“也是呢，我知道了”<br />
“结束之后再叫我唷，我要请佣兵团来搬木材那些的”<br />
“了解”<br />
<br />
接着，战斗之后一个月。<br />
我们一边保持警戒一边为战斗做准备，但并没有再发生战斗。<br />
<br />
已经不会有战斗了吧。<br />
因此，我让洛克希、希露菲、还有扎诺巴那几个，回去夏利亚了。艾丽丝也以担任她们的护卫为名目让她回去了。<br />
不过因为零式的召唤魔法阵与避难时使用到的魔法阵都在奥尔斯蒂德与亚历之战中消失了，所以只好再将他们送往佩尔基乌斯那里。<br />
而他们将会负责事务所的重建，以及恢复通信石版、转移魔法阵的工作。<br />
<br />
夏利亚那边好像没有出什么事的样子。<br />
柜台的精灵妹妹也平安无事。<br />
大概只有躺在事务所地下的武器防具类、还有奥尔斯蒂德每天所写的文件被埋在瓦砾中的程度而已。<br />
<br />
前去避难的斯佩尔多族人们从国境附近，透过第二都市伊雷尔的魔法阵的重新连接而回来了。<br />
在那之后，正式地受到了毕坚利鲁王国的欢迎。<br />
毕坚利鲁王国对于将斯佩尔多族做为国民迎接的事，抱持肯定的态度。<br />
虽然也有一部份是因为失去了第三都市与鬼神的他们目前不是能说NO的状态吧。<br />
做为迎接到国内的处置，对方提出了要从村子里派出最少３人，为国家服务，这样的追加条件。<br />
这部份，鬼族好像也是一样的。<br />
而那三人的挑选也暂且结束了，现在派遣去复兴村子而动身了。<br />
要是能这样平安地持续复兴的话，毕坚利鲁王国国内就能有斯佩尔多族的栖身之所了吧。<br />
<br />
把使徒全部打倒，又让斯佩尔多族、鬼族及毕坚利鲁王国成为同伴。<br />
我们，赢了。<br />
但是，到底这能算是真正的胜利了吗。<br />
<br />
“卢迪乌斯阁下”<br />
“翔德尔先生”<br />
<br />
当我一边想着一边持续砍伐森林时，不知何时翔德尔站到了我身后。<br />
不光是翔德尔。<br />
还能看到基列奴、伊泽露缇及东迦。<br />
<br />
翔德尔是在战斗完大概经过１０天之后回来的。<br />
他在跟斗神的战斗中被击落海中，漂流到鬼岛，并在那里努力恢复的样子。<br />
该说他和斗神战斗之后，真亏他能活着回来吗。<br />
<br />
“您辛苦了。有什么事吗？”<br />
“没有，只是，我想我们差不多该回阿苏拉了。所以来向您道别”<br />
“……嗯”<br />
<br />
翔德尔他们的工作完成了。<br />
他们终究还是艾丽爱尔的部下。<br />
要是已经没有战事的话，他们不回去也不行。<br />
<br />
“翔德尔先生。真的非常感谢你。要是没有你在的话，就没有今天这个成果了”<br />
“要道谢的话，就和艾丽爱尔陛下说吧”<br />
“那是当然。请帮我转告陛下，之后要是有发生什么事请先通知我。我一定鼎力相助”<br />
“我知道了”<br />
<br />
翔德尔、东迦、基列奴、伊泽露缇。<br />
一个个都是王级以上的强力剑士。<br />
对替我准备了如此战力的艾丽爱尔，有着说不尽的感谢。<br />
<br />
“基列奴，也非常谢谢你”<br />
“不用道谢……不过，下次有扫墓的话我也想参加”<br />
“我知道了。请你再等一下”<br />
<br />
基列奴只说一句话。<br />
<br />
“东迦也多谢了。我掉到谷底的时候，要是没有你在的话我就死定了”<br />
“喔”<br />
“要是有什么私人上的困难的话请跟我说。我想报答救命之恩”<br />
“喔！”<br />
<br />
东迦虽然只会说喔，不过好像稍微有点寂寞。<br />
<br />
“也非常感谢伊泽露缇小姐。当时要是没有你挡在那边的话，我也是死定了”<br />
“不会，这次的外派里我也学到了很多。我才要向你道谢”<br />
<br />
伊泽露缇优雅地行礼之后，露出了开朗的微笑。<br />
还是一如既往漂亮的人。<br />
听说她还是未婚，真想知道阿苏拉的男人们到底都在干什么呢。<br />
<br />
“也请帮我向医师团的各位说一声”<br />
“好的，那……告辞了”<br />
<br />
翔德尔行了一礼之后转身。<br />
但，那背影，让我想起了忘记说的事，而叫住了他。<br />
<br />
“那个，阿道菲大人的事，我很遗憾”<br />
<br />
翔德尔回来了。<br />
但是，回来的，只有他而已。<br />
阿道菲依然下落不明。<br />
要是漂到海上的话，几年之内应该都找不到人吧。<br />
摩尔也是一样。<br />
<br />
“……母亲那方面的事，不用去操心吧。我想再过一阵子，又会突然冒出来了。真的让人遗憾的，我想是鬼神阁下”<br />
“……没错呢”<br />
<br />
鬼神已经确认身亡。<br />
他全力地迎战了斗神。<br />
然而，他并非不死魔族。最后，耗尽力气，身亡了。<br />
明明好不容易才和鬼神谈和的。<br />
<br />
“只不过，对死者感到悔恨也没有用”<br />
“是呢。不向前看不行”<br />
<br />
我和他约定过了。<br />
自己要是死了的话，就要保护幸存的鬼族。<br />
目前虽然没有会威胁到鬼族的存在，不过要是发生什么事时，即使只有约定，我也会去遵守。<br />
<br />
“那，失礼了”<br />
“好的。辛苦你了”<br />
“啊啊对了……亚历就麻烦你了”<br />
“……好的”<br />
<br />
翔德尔这么说完，便转身离去了。<br />
<br />
这时，就好像要和他们轮替一样，我看到克里夫正走过来。<br />
艾丽娜莉兹也一起。<br />
<br />
“卢迪乌斯”<br />
“克里夫学长”<br />
“他们也要回去吗”<br />
“是的，克里夫学长也是吗？”<br />
“嗯。毕竟好像差不多已经结束了……虽然最后还是不知道传染病的病因，不过经过了一个月也没有复发，居住的地方也换了……所以我暂时要回去了”<br />
<br />
克里夫也照顾了我不少。<br />
要是没有他在的话，传染病就没办法治好了吧。<br />
说是传染病，但其实是冥王的杰作也说不定就是了。<br />
<br />
“克里夫学长。非常谢谢你。你要是没有来的话，真不知道会变得怎么样……”<br />
“嘛，既然是你的话，我想自己一个人也能解决就是了呢。要是传染病又再复发的话，再连络我”<br />
“好的……老是受克里夫学长的帮忙，真不知道该怎么道谢才好”<br />
“我能放下莉兹与克莱普到米里斯奋斗，也是因为你的家人在夏利亚替我照顾他们嘛。彼此彼此”<br />
<br />
你能这么说，我很感激呢。<br />
<br />
“那，再会。回去的时候我打算去你家一趟，有什么想转达的吗？”<br />
“帮我说，我马上就回去”<br />
“没问题”<br />
<br />
克里夫这么说完，转身离去。<br />
而最后艾丽娜莉兹向我抛了个媚眼。虽然也受了她的照顾，不过什么都没说呢……。嘛，毕竟就是附近邻居，就用行动表示吧。<br />
<br />
不过，这次真的得到许多人的帮助呢。<br />
<br />
首先是克里夫。要是没有他的话，斯佩尔多族或许就被传染病给灭绝了。<br />
<br />
还有翔德尔跟东迦。要是没有他们，我就没办法站在这里了吧。<br />
<br />
阿道菲的时机也是十分绝妙。<br />
阿道菲之手，还有在恰到好处的时机对鬼岛发动的攻击。<br />
能够几乎无伤的胜利，可说是多亏了阿道菲的福。<br />
要是让那个阿道菲就这么下落不明下去的话，也有点太忘恩负义了，等稳定下来之后跟大家一起到海边寻找好了。<br />
<br />
战斗结束后，大家都回去了。<br />
就和大型的活动结束之后，各自解散的感觉类似。<br />
真的很寂寞。<br />
<br />
“好”<br />
<br />
当我在想着这些的时候，森林的开垦也完成了。<br />
眼前是一片美丽的大地。<br />
被连根拔起的大树，正用土魔法整齐地排在一起。<br />
就我来说，做得挺不错的。<br />
<br />
“好了，再来和爱莎……喔”<br />
<br />
一转身，刚好看到瑞杰尔德和诺伦正在走来。<br />
